public class task7 {
    public static void main(String[] args) {
        System.out.println("'a' isDigit('a') is " + Character.isDigit('a')); // checks if 'a' is a digit
        System.out.println("'a' isLetter('a') is " + Character.isLetter('a')); // checks if 'a' is a letter
        System.out.println("'a' isLowerCase('a') is " + Character.isLowerCase('a')); // checks if 'a' is lowercase
        System.out.println("'a' isUpperCase('a') is " + Character.isUpperCase('a')); // checks if 'a' is uppercase
        System.out.println("'T' toLowerCase('T') is " + Character.toLowerCase('T')); // converts 'T' to lowercase
        System.out.println("'q' toUpperCase('q') is " + Character.toUpperCase('q')); // converts 'q' to uppercase
    }
}
